extensión rural unido con CENICAFE, y soportado en el SICA, son en conjunto una ventaja
competitiva de los caficultores colombianos. Además del conocimiento técnico, la
formación empresarial es determinante en la competitividad del negocio cafetero. La
formación a formadores y el desarrollo de nuevas herramientas de extensión que integren
TIC, hace parte del fortalecimiento de destrezas para transmitir conocimientos, desarrollar
capacidades integrales entre los productores y aumentar la efectividad de sus servicios.
Los caficultores vamos a prepararnos para ser mejores administradores de nuestro capital.</p>
                            
