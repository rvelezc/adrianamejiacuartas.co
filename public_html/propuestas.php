gran alianza público privada a través de una PACC – Política Agrícola Cafetera
Colombiana- que empieza en Colombia entre el gobierno nacional y la Federación y se
extiende a la participación con recursos financieros y compromisos claros de otros
elementos de la cadena de suministro. Esto con el fin que todo el sector centrado en la
viabilidad del negocio permita a los cafeteros continuar en sus tierras, manteniendo su
tradición, produciendo el mejor café del mundo y continuar generando tejido social en la
ruralidad colombiana.</p>
                            
