al punto de compra y de venta de Federación al cliente final, simplificando, es la
rentabilidad que se registra en la actividad comercial de la Federación. Esta diferencia
corresponde a un ahorro sobre el cual los caficultores recibirán dividendos. Vamos a
diseñar un mecanismo de vanguardia para el retorno de la inversión a los accionistas
cafeteros.
</p>
                            
