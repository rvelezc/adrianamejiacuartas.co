+ TENDENCIAS DE CONSUMO EN MERCADOS INTERNACIONALES
</h3>
                            <p>El mercado percibe a Colombia como productor de café confiable, proveedor de
calidad y socio en sostenibilidad. En mercados emergentes, existe aún trabajo por hacer
en posicionamiento y promoción. El café soluble registra el mayor crecimiento en esos
mercados, oportunidad que seguiremos explotando con Buencafé. Las tendencias de
consumo son cafés de origen, monodosis y con estándares sostenibles. Trazabilidad,
conectividad y transparencia son el nuevo terreno de cafés especiales en tiendas de café,
