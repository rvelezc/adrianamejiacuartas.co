democracia cafetera como la garante de la legitimidad institucional. Estoy convencida
del impacto positivo de la agremiación de los actores de la caficultura en la agregación
de valor conjunto. Vamos a convocarlos para que definamos objetivos comunes y
acciones focalizadas. Aspiro fortalecer la representatividad gremial de la Federación y su
