Colombia como sinónimo de sostenibilidad.
</p>
                            
                            <h3>5. GARANTÍA DE COMPRA</h3>
                            <p>La garantía de compra asegura la compra del café cerca al caficultor, pone un piso al
precio en el mercado y brinda liquidez al productor a través de pagos de contado. La
garantía de compra es un articulador de valor vertical de la cadena. El caficultor percibe
más valor que el precio de venta a su institución, pues hace parte de ella, y los dividendos
recibidos en forma de servicios de extensión rural o en bonificaciones posteriores con base
al precio de venta al cliente final, superan el precio de transacción en el momento de la
entrega del café al punto de compra. Defenderemos la garantía de compra como parte
integral y fundamental en la formación de valor para el caficultor.
</p>
                            
                            <h3>6. MECANISMOS DE ESTABILIZACIÓN DE PRECIOS</h3>
                            <p>Mecanismos como las fijaciones de precio para entrega futura del café, ofrece más
alternativas de precio que las de un día de mercado en la plaza, pues se tiene la variable
tiempo a favor. El caficultor tendrá la posibilidad de fijar el precio de su café con
anticipación, a un nivel deseable, y podrá aprovechar la volatilidad del mercado a su
favor. El gran reto con este tipo de medidas es hacer que sea un instrumento al alcance
de todos los caficultores. Esfuerzos encaminados a fortalecer y subsidiar la oferta y
demanda de mecanismos de cobertura del precio y de la tasa de cambio, sin duda es el
camino correcto. Apoyo a estos mecanismos es un tema que toca La Misión y que
propone como favorable, con lo cual estamos de acuerdo. Esta es una de las opciones
pero no puede ser la única.</p>
                            
